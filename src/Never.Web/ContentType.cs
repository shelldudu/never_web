namespace Never.Web
{
    /// <summary>
    /// Content-typeHTTP 标头的值
    /// </summary>
    public static class ContentType
    {
        /// <summary>
        /// The json
        /// </summary>
        public const string Json = "application/json";

        /// <summary>
        /// The XML
        /// </summary>
        public const string Xml = "application/xml";

        /// <summary>
        /// The text XML
        /// </summary>
        public const string TextXml = "text/xml";

        /// <summary>
        /// The text plain
        /// </summary>
        public const string TextPlain = "text/plain";

        /// <summary>
        /// The default
        /// </summary>
        public const string Default = "application/x-www-form-urlencoded";

        /// <summary>
        /// The javascript
        /// </summary>
        public const string Javascript = "application/x-javascript";
    }
}